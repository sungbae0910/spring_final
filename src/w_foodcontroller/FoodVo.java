public class FoodVo {
	int foodCode;
	String foodName;
	int foodPrice;
	String foodType;
	String foodOriFile;
	String foodSysFile;


	public int getFoodCode() {
		return foodCode;
	}

	public void setFoodCode(int foodCode2) {
		this.foodCode = foodCode2;
	}

	public String getFoodName() {
		return foodName;
	}

	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}

	public int getFoodPrice() {
		return foodPrice;
	}

	public String getFoodType() {
		return foodType;
	}

	public void setFoodType(String foodType) {
		this.foodType = foodType;
	}

	public void setFoodPrice(int foodPrice) {
		this.foodPrice = foodPrice;
	}

	public String getFoodOriFile() {
		return foodOriFile;
	}

	public void setFoodOriFile(String foodOriFile) {
		this.foodOriFile = foodOriFile;
	}

	public String getFoodSysFile() {
		return foodSysFile;
	}

	public void setFoodSysFile(String foodSysFile) {
		this.foodSysFile = foodSysFile;
	}

	
	
}
